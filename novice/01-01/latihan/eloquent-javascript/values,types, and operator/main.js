// NUMBERS
let myNumber = 10;
let fractNumber = 9.1;
console.log(myNumber);
console.log(fractNumber);

// ARITHMETIC
console.log(myNumber * fractNumber);

// SPECIAL NUMBERS
console.log(Infinity);
console.log(-Infinity);
console.log(NaN);

// STRING
console.log("Hello World");
console.log("Hello World \n World, HI!");

// UNARY OPERATORS
console.log(typeof 4.5);
console.log(typeof "abcdefgh");
console.log(-(10 - 2));

// BOOLEAN VALUES
console.log(4 > 5);
console.log(10 > 5);
console.log("Itchy" != "Scratchy");

// LOGICAL OPERATOR
console.log(true && false);
console.log(true && true);

// EMPTY VALUES
console.log(null);
console.log(undefined);

// AUTOMATIC TYPE CONVERSION
console.log(8 * null);
console.log("5" - 1);
console.log("5" + 1);
console.log(false == 0);

// Short-circuiting of logical operators
console.log(null || "user");
console.log("Agnes" || "user");
